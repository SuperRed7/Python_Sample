print('hello world 1')
print("hello world 2")
print('''hello world 3''')
print("""hello world 4""")

a = 100
b = 50
print(a)
print(a * b)

# 不换行一次性输出多个数据
print(a, b, "hello world 5")

# 输出多行字符串,或直接复制文本输出
c = (
    '''
hello world 6

hello world 7

hello world 8
'''
)
print(c)

d = (
    "hello world 9 " \
    "hello world 10 " \
    "hello world 11 " \
    )
print(d)
